    <script>
        function popUp() {
            document.getElementById('popup').className += ' block';
            document.getElementById('overlay_popup').className += ' block'; 
        };
        function popUpOff() {
            document.getElementById('popup').className ='popup';
            document.getElementById('overlay_popup').className ='overlay_popup'; 
        };
    </script>
